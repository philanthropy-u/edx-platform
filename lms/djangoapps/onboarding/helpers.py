def reorder_registration_form_fields(fields):
    required_order = {
        'first_name': 0,
        'last_name': 1,
        'username': 2,
        'email': 3,
        'password': 4,
        'confirm_password': 5,
        'is_currently_employed': 6,
        'organization_name': 7,
        'is_poc': 8,
        'org_admin_email': 9,
        'honor_code': 10,
        'opt_in': 11,
    }

    for idx, field in enumerate(fields):
        order = required_order.get(field['name'])
        if order >= 0:
            field['order'] = order
        else:
            field['order'] = idx

    required_order = sorted(fields, key=lambda k: k['order'])
    return required_order


